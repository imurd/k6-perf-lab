// ============================================================
//  03. STRESS-ТЕСТ (стресс, "ищем потолок")
//  Цель: постепенно наращивать нагрузку ВЫШЕ ожидаемой, пока
//  система не начнёт деградировать (растёт латентность / ошибки).
//  Так находят предел пропускной способности ("сколько держит").
//  Запуск:  k6 run 03-stress.js
//
//  ВАЖНО: здесь мы НАМЕРЕННО ждём провала порогов — это нормально.
//  Точка, где p95 взлетает или пойдут ошибки, и есть "потолок".
// ============================================================

import http from "k6/http";
import { check } from "k6";
import { BASE_URL } from "./lib/config.js";

export const options = {
  stages: [
    { duration: "1m", target: 50 },   // разогреваемся до 50
    { duration: "1m", target: 100 },  // 100
    { duration: "1m", target: 200 },  // 200
    { duration: "1m", target: 400 },  // 400 — тут уже, скорее всего, начнёт больно
    { duration: "1m", target: 0 },    // остываем
  ],
  // Порог мягкий и НЕ прерывающий: хотим досмотреть, где именно ломается,
  // а не упасть на первой же превышенной цифре.
  thresholds: {
    http_req_failed: ["rate<0.05"],   // терпим до 5% ошибок
  },
};

export default function () {
  const res = http.get(`${BASE_URL}/public/crocodiles/`);
  check(res, { "статус 200": (r) => r.status === 200 });
  // Без sleep: на стрессе часто убирают паузы, чтобы выжать максимум запросов.
}
